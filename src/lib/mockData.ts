function randomDate(): string {
  const base = new Date('2026-04-10T09:00:00Z').getTime()
  const offset = Math.floor(Math.random() * 3 * 60 * 60 * 1000) // 3時間以内
  return new Date(base + offset).toISOString()
}

export function generateMockPosts(count = 380): Post[] {
  const posts: Post[] = []
  let index = 0

  while (posts.length < count) {
    for (const { category, texts } of TEMPLATES) {
      for (const text of texts) {
        if (posts.length >= count) break
        posts.push({
          id: `mock-${index++}`,
          text,
          category,
          keywords: [],
          session_id: `mock-session-${index}`,
          created_at: randomDate(),
        })
      }
      if (posts.length >= count) break
    }
  }

  return posts.sort(
    (a, b) => new Date(b.created_at).getTime() - new Date(a.created_at).getTime()
  )
}
